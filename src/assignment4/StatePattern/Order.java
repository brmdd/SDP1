package assignment4.StatePattern;

public class Order {
    private OrderState state = new NewOrder();

    public void setState(OrderState state) {
        this.state = state;
    }

    public void previousState() {
        state.prev(this);
    }

    public void nextState() {
        state.next(this);
    }

    public void printStatus() {
        state.printStatus();
    }
}
